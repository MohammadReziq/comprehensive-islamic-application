/// تصنيف الحديث لاستخدامه في الفلترة أو اختيار الشاشة.
enum HadithCategory {
  /// فضل الصلاة وعماد الدين
  virtueOfPrayer,

  /// صلاة الجماعة والمسجد
  congregation,

  /// تربية الأولاد على الصلاة (ولي الأمر)
  teachingChildren,

  /// المحافظة على الصلاة والخشوع
  steadfastness,

  other,
}

/// امتداد لعرض الاسم العربي للتصنيف إن احتجته في الواجهة.
extension HadithCategoryExtension on HadithCategory {
  String get labelAr {
    switch (this) {
      case HadithCategory.virtueOfPrayer:
        return 'فضل الصلاة';
      case HadithCategory.congregation:
        return 'صلاة الجماعة والمسجد';
      case HadithCategory.teachingChildren:
        return 'تربية الأولاد على الصلاة';
      case HadithCategory.steadfastness:
        return 'المحافظة على الصلاة';
      case HadithCategory.other:
        return 'أخرى';
    }
  }
}
